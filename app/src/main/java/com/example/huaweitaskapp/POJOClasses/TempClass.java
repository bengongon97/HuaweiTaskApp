package com.example.huaweitaskapp.POJOClasses;

public class TempClass {
    double day;
    double night;

    public TempClass(double day, double night) {
        this.day = day;
        this.night = night;
    }

    public double getDay() {
        return day;
    }

    public double getNight() {
        return night;
    }
}
